// Helper functions for user posts storage
export const getStoredPosts = () => {
  try {
    const raw = localStorage.getItem("creatorlens_user_posts");
    return raw ? JSON.parse(raw) : [];
  } catch (e) {
    console.error("Error reading user posts from localStorage", e);
    return [];
  }
};

export const saveUserPost = (post) => {
  try {
    const current = getStoredPosts();
    const updated = [post, ...current];
    localStorage.setItem("creatorlens_user_posts", JSON.stringify(updated));
    return updated;
  } catch (e) {
    console.error("Error saving user post to localStorage", e);
    return [];
  }
};

export const getAllPosts = () => {
  const userPosts = getStoredPosts();
  return [...userPosts, ...samplePosts];
};
